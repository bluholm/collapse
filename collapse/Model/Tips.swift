import Foundation

/// The `Tips` struct represents a tip object that contains information about a specific tip.
struct Tips {
    
    /// The title of the tip.
    let title: String
    
    /// A Boolean value that indicates whether the tip should be highlighted.
    let highlight: Bool
    
    /// The name of the icon associated with the tip.
    let icon: String
    
    /// The language of the tip.
    let langage: String
    
    /// The unique identifier of the tip.
    let id: String
    
    /// A description of the tip.
    let description: String
    
}
