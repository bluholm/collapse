Une autre option facile pour collecter de l'eau est de profiter de la transpiration des plantes. Il s'agit du processus par lequel l'humidité est transportée des racines d'une plante vers la face inférieure de ses feuilles. De là, elle s'évapore dans l'atmosphère, mais vous allez récupérer l'eau avant qu'elle ne le fasse.







L'eau du sol s'accumule dans une illustration souterraine.






Instructions pour votre alambic souterrain :


Fournitures

Récipient (le plus grand que vous avez)
Feuille de plastique transparent
Outil de creusement
Roches
Facultatif : quelque chose qui sert de tube ou de paille pour boire (paille de CamelBak, bambou ou autre plante).
Instructions

Trouve un endroit qui reçoit la lumière du soleil la majeure partie de la journée.
Creusez une fosse en forme de bol de 1m de large sur 60cm de profondeur. Creusez un autre petit trou à l'intérieur pour le récipient.
Facultatif : Fixez le tube d'abreuvement au fond du récipient. Si vous n'en avez pas, passez cette étape.
Placez le récipient dans la fosse et faites sortir le tuyau du trou.
Couvrez le trou avec du plastique, et utilisez des pierres et de la terre pour le maintenir en place.
Mettez une petite pierre au centre de votre feuille, de façon à ce qu'elle pende et forme un cône inversé au-dessus du récipient.
Si vous avez un tube, buvez directement à partir de celui-ci. Sinon, récupérez le récipient par le fond, et remettez-le en place lorsque vous aurez stocké l'eau ailleurs.
Il y a presque toujours de l'humidité dans le sol à cette profondeur. Elle réagira à la chaleur du soleil pour produire de la condensation, qui s'accumulera sur le plastique. Le cône inversé force cette condensation à descendre dans votre récipient. Vous pouvez vous attendre à recueillir 0,5-1 litre par jour, donc il vous faudra plus d'un récipient (ou une autre source) pour assurer l'approvisionnement d'une journée entière.
